def write_chunked_outputs(root: Path, source: str) -> list[Path]:
    chunks: list[Path] = []
    source_bytes = source.encode("utf-8")
    for index, start in enumerate(range(0, len(source_bytes), CHUNK_SIZE), start=1):
        chunk = source_bytes[start:start + CHUNK_SIZE]
        path = root / output_part_name(index)
        path.write_text("return " + lua_quote_bytes(chunk) + "\n", encoding="utf-8", newline="\n")
        chunks.append(path)

    install_path = root / output_install_name()
    install_path.write_text(chunk_bootstrap_source(len(chunks)), encoding="utf-8", newline="\n")
    return [install_path, *chunks]


def pastebin_code_from_text(text: str) -> str | None:
    patterns = [
        r"pastebin\.com/raw/([\w_-]+)",
        r"pastebin\.com/([\w_-]+)",
        r"code:\s*([\w_-]+)",
        r"Code:\s*([\w_-]+)",
    ]
    for pattern in patterns:
        match = re.search(pattern, text)
        if match:
            return match.group(1)
    return None


def read_dev_key(root: Path, args: argparse.Namespace) -> str | None:
    if args.dev_key:
        return args.dev_key.strip()
    for name in ["pastebin_dev_key"]:
        path = root / name
        if path.exists():
            return path.read_text(encoding="utf-8").strip()
    return None


def upload_with_api(path: Path, dev_key: str, args: argparse.Namespace) -> tuple[str, str]:
    if requests is None:
        raise RuntimeError("Python package 'requests' is required for --upload. Install it with: python3 -m pip install requests")

    fields = {
        "api_dev_key": dev_key,
        "api_option": "paste",
        "api_paste_code": path.read_text(encoding="utf-8"),
        "api_paste_name": path.name,
        "api_paste_private": args.private,
        "api_paste_expire_date": args.expire,
        "api_paste_format": "lua",
    }
    if args.user_key:
        fields["api_user_key"] = args.user_key

    headers = {
        "User-Agent": args.user_agent,
        "Accept": "text/plain, */*;q=0.8",
        "Origin": "https://pastebin.com",
        "Referer": "https://pastebin.com/",
    }

    session = requests.Session()
    response = session.post(
        "https://pastebin.com/api/api_post.php",
        data=fields,
        headers=headers,
        timeout=args.timeout,
    )
    text = response.text

    if response.status_code == 403:
        raise RuntimeError(
            "Pastebin returned HTTP 403. This is usually Cloudflare/API blocking, "
            "an invalid dev key, or a paste privacy/user-key mismatch. Response: "
            + text[:300]
        )
    if response.status_code >= 400:
        raise RuntimeError(f"Pastebin returned HTTP {response.status_code}: {text[:300]}")

    if text.startswith("Bad API request"):
        raise RuntimeError(text)
    code = pastebin_code_from_text(text)
    if not code:
        raise RuntimeError("PastebinCodeNotFound: " + text)
    return code, text


def upload_batch_install_streaming(root: Path, source: str, args: argparse.Namespace) -> dict[str, object]:
    dev_key = read_dev_key(root, args)
    if not dev_key:
        raise RuntimeError("Pastebin dev key required: pass --dev-key or create pastebin_dev_key")

    codes: list[str] = []
    source_bytes = source.encode("utf-8")
    total_chunks = math.ceil(len(source_bytes) / CHUNK_SIZE)

    for index in range(1, total_chunks + 1):
        start = (index - 1) * CHUNK_SIZE
        chunk = source_bytes[start:start + CHUNK_SIZE]
        path = root / output_part_name(index)
        path.write_text("return " + lua_quote_bytes(chunk) + "\n", encoding="utf-8", newline="\n")

        print(f"Uploading chunk {index}/{total_chunks}: {path.name}")
        try:
            code, _ = upload_with_api(path, dev_key, args)
        finally:
            if not args.keep_chunks and path.exists():
                path.unlink()
        code = code[:8]
        codes.append(code)
        print(f"  code: {code}")

    batch_path = root / BATCH_INSTALL_FILE
    batch_path.write_text(batch_install_source(codes), encoding="utf-8", newline="\n")

    print(f"Uploading batch installer: {BATCH_INSTALL_FILE}")
    final_code, _ = upload_with_api(batch_path, dev_key, args)
    final_code = final_code[:8]
    return {
        "path": BATCH_INSTALL_FILE,
        "code": final_code,
        "chunks": codes,
    }


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Package HyperCubeServer for Pastebin.")
    parser.add_argument("--source-root", default=None, help="Source root to package. Defaults to this script's folder.")
    parser.add_argument("--upload", action="store_true", help="Upload chunks and final batch installer to Pastebin.")
    parser.add_argument("--single", action="store_true", help=f"Write {OUTPUT_FILE}.")
    parser.add_argument("--dev-key", default=None, help="Pastebin API dev key.")
    parser.add_argument("--user-key", default=None, help="Optional Pastebin user key.")
    parser.add_argument("--private", default="1", choices=["0", "1", "2"], help="Pastebin privacy: 0 public, 1 unlisted, 2 private.")
    parser.add_argument("--expire", default="N", help="Pastebin expiration, e.g. N, 1D, 1W, 1M.")
    parser.add_argument("--timeout", type=int, default=60, help="HTTP timeout in seconds.")
    parser.add_argument("--keep-chunks", action="store_true", help="Keep temporary chunk files after --upload.")
    parser.add_argument(
        "--user-agent",
        default="Mozilla/5.0 (Windows NT 10.0; Win64; x64) HyperCubePackager/1.0",
        help="User-Agent header used for Pastebin API uploads.",
    )
    return parser.parse_args(argv)


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    root = Path(args.source_root).resolve() if args.source_root else Path(__file__).resolve().parent

    cleanup_outputs(root)
    files = collect(root)
    total = sum(int(file["size"]) for file in files)
    source = installer_source(files)

    print(f"Source root: {root}")
    print(f"Files packed: {len(files)}")
    print(f"Raw bytes: {total}")
    print(f"Installer bytes: {len(source.encode('utf-8'))}")

    if args.upload:
        try:
            result = upload_batch_install_streaming(root, source, args)
        except Exception as exc:
            print(f"Upload failed: {exc}")
            return 1
        print("")
        print(f"Wrote {result['path']}")
        print(f"Final installer Pastebin code: {result['code']}")
        print("Install command:")
        print(f"pastebin run {result['code']}")
        return 0

    if args.single:
        (root / OUTPUT_FILE).write_text(source, encoding="utf-8", newline="\n")
        print(f"Wrote {OUTPUT_FILE}")
    else:
        print(f"Skipping {OUTPUT_FILE} to save disk space. Pass --single to write it.")

    outputs = write_chunked_outputs(root, source)
    print("Pastebin-safe chunk files:")
    for path in outputs:
        print(f"  {path.name} ({path.stat().st_size} bytes)")
    print("Run with --upload to upload chunks and generate a final batch installer code.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
