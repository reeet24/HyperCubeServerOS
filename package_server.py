OUTPUT_FILE = "hypercube_server_pastebin.lua"
CHUNK_SIZE = 240000
BATCH_INSTALL_FILE = "hypercube_server_pastebin_batch_install.lua"

INCLUDE_ROOTS = [
    "Kernal",
    "appstore",
    "installer",
    "docs",
    "init.lua",
    "startup.lua",
    "README.md",
    "checklist.md",
    "update_server.lua",
]

EXCLUDE = {
    OUTPUT_FILE,
    "logs",
    "user",
    "hypercube_db",
    "package_server.lua",
    "package_server.py",
    "pastebin_dev_key",
    "pastebin_dev_key.lua",
}


def output_part_name(index: int) -> str:
    return f"hypercube_server_pastebin_chunk_{index:02d}.lua"


def output_install_name() -> str:
    return "hypercube_server_pastebin_install.lua"


def normalize(path: str | Path) -> str:
    return str(path).replace("\\", "/").lstrip("./").lstrip("/")


def is_excluded(path: str) -> bool:
    path = normalize(path)
    if path in EXCLUDE:
        return True
    return any(path.startswith(excluded + "/") for excluded in EXCLUDE)


def lua_quote_bytes(data: bytes) -> str:
    out: list[str] = ['"']
    for byte in data:
        if byte == 34:
            out.append('\\"')
        elif byte == 92:
            out.append("\\\\")
        elif byte == 10:
            out.append("\\n")
        elif byte == 13:
            out.append("\\r")
        elif byte == 9:
            out.append("\\t")
        elif 32 <= byte <= 126:
            out.append(chr(byte))
        else:
            out.append(f"\\{byte:03d}")
    out.append('"')
    return "".join(out)


def lua_quote_text(text: str) -> str:
    return lua_quote_bytes(text.encode("utf-8"))


def cleanup_outputs(root: Path) -> None:
    for name in [OUTPUT_FILE, output_install_name(), BATCH_INSTALL_FILE]:
        path = root / name
        if path.exists():
            path.unlink()
    for index in range(1, 100):
        path = root / output_part_name(index)
        if path.exists():
            path.unlink()


def collect_tree(root: Path, relative: str, files: list[dict[str, object]]) -> None:
    relative = normalize(relative)
    if is_excluded(relative):
        return

    path = root / relative
    if path.is_dir():
        for child in sorted(path.iterdir(), key=lambda item: item.name):
            child_relative = child.relative_to(root).as_posix()
            collect_tree(root, child_relative, files)
        return

    data = path.read_bytes()
    files.append({
        "path": relative,
        "data": data,
        "size": len(data),
    })


def collect(root: Path) -> list[dict[str, object]]:
    files: list[dict[str, object]] = []
    for include in INCLUDE_ROOTS:
        if (root / include).exists():
            collect_tree(root, include, files)
    files.sort(key=lambda file: str(file["path"]))
    return files


